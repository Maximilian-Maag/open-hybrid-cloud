interface NavItem {
  href: string
  label: string
  icon: React.ReactNode
  minRole?: Role
  exact?: boolean
}

const roleLevel: Record<Role, number> = {
  project_manager: 0,
  admin: 1,
  root: 2,
}

function hasRole(userRole: Role, minRole: Role): boolean {
  return roleLevel[userRole] >= roleLevel[minRole]
}

